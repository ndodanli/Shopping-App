    private void uyeOlUyeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uyeOlUyeButtonActionPerformed
        String parola = new String(uyeOlparolaText.getPassword());
        String parolaTekrar = new String(uyeOlParolaTekrarText.getPassword());
        if (uyeOlKAdiText.getText().isEmpty() || parola.isEmpty() || parolaTekrar.isEmpty() || uyeOlPostaText.getText().isEmpty() || uyeOlTelText.getText().isEmpty()) {
            uyeOlBilgiL.setText("Boş alan bırakmayınız");
        } else if (!parola.equals(parolaTekrar)) {
            uyeOlBilgiL.setText("Parolalar eşleşmiyor");
        } else {
            try {
                sessionObj = buildSessionFactory().openSession();
                sessionObj.beginTransaction();
                Query query = sessionObj.createQuery("select count(1) from Kullanici K where K.kullaniciAdi=:gelenKAd");  //kullanıcının girdiği kullanıcı adı ile alınmış kayıt olup olmadığını anlamak için sorgu oluşturuyoruz
                query.setParameter("gelenKAd", uyeOlKAdiText.getText()); //hql sorgusunda parametre olarak kullandığımız gelenKAd'a kullanıcının girdiği kullanıcı adını atıyoruz
                control = (long) query.uniqueResult();   //tek bir veri alacağımız için uniqueResult() metodu ile sorguyu gerçekleştiriyoruz
                if (control != 0) { //eğer kullanıcı adı kullanılmış ise sorgumuz 0'dan farlı(1) döneceğinden bu şekilde kontrol ediyoruz
                    uyeOlBilgiL.setText("Kullanıcı adı geçersiz");
                } else {
                    sessionObj.save(new Kullanici(uyeOlKAdiText.getText(), parola, "user", uyeOlPostaText.getText()));
                    uyeOlBilgiL.setText("Kayıt başarılı");
                }
                sessionObj.getTransaction().commit();
            } catch (Exception sqlException) {
                if (null != sessionObj.getTransaction()) {
                    sessionObj.getTransaction().rollback();
                }
                sqlException.printStackTrace();
            } finally {
                if (sessionObj != null) {
                    sessionObj.close();
                }
            }
        }
    }//GEN-LAST:event_uyeOlUyeButtonActionPerformed

