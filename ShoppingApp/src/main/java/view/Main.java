    private void satinAlButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_satinAlButtonActionPerformed
        float sepetUcret = 0, yeniBakiye = 0;
        Kullanici kullanici = LoginData.getKullaniciBilgi();
        for (Urun urun : sepettekiUrunler) {    //sepetteki ürünlerin toplam fiyatını hesaplıyoruz
            sepetUcret += urun.getUcret();
        }
        yeniBakiye = kullanici.getCuzdan() - sepetUcret;    //alışverişten sonra yüklenecek yeni bakiyeyi belirliyoruz
        if (sepettekiUrunler.isEmpty()) {
            sepetBilgiL.setText("Sepette ürün yok");
        } else if (kullanici.getCuzdan() < sepetUcret) {    //kullanıcının cüzdanında yeterli bakiye yoksa
            sepetBilgiL.setText("Yetersiz bakiye");
        } else {    //kullanıcının cüzdanında yeterli bakiye varsa
            try {
                sessionObj = sessionFactoryObj.openSession();
                sessionObj.beginTransaction();
                String hql = "update Kullanici set kullanici_cuzdan=:yeniBakiye where id=:kullaniciId"; //yeni bakiyeyi veri tabanında güncelliyoruz
                Query query = sessionObj.createQuery(hql);
                query.setParameter("kullaniciId", kullanici.getId());
                query.setParameter("yeniBakiye", yeniBakiye);
                int result = query.executeUpdate();
                cuzdanBilgiL.setText(String.valueOf(yeniBakiye));
                kullanici.setCuzdan(yeniBakiye);
                LoginData.setKullaniciBilgi(kullanici);
                sepettekiUrunler.clear();
                urunSepet.removeAll();
                urunSepet.revalidate();
                urunSepet.repaint();
                sepetBilgiL.setText("Satın alma başarılı");
                sessionObj.getTransaction().commit();
            } catch (Exception sqlException) {
                if (null != sessionObj.getTransaction()) {
                    sessionObj.getTransaction().rollback();
                }
                sqlException.printStackTrace();
            } finally {
                if (sessionObj != null) {
                    sessionObj.close();
                }
            }
        }
    }//GEN-LAST:event_satinAlButtonActionPerformed

