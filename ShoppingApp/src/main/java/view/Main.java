    private void girisYapButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_girisYapButtonActionPerformed
        //giriş yap butonuna tıklandığında tetiklenen metod
        String parola = new String(girisParolaText.getPassword());  //parolamızı alıyoruz
        if (GirisKAdiText.getText().isEmpty() || parola.isEmpty()) { //boş alan varsa uyarıyoruz
            girisBilgiL.setText("Boş alan bırakmayınız");
        } else {
            try {
                sessionObj = buildSessionFactory().openSession();   //yeni bir session aç
                sessionObj.beginTransaction();  //işlemi başlat
                Query query = sessionObj.createQuery("from Kullanici K where K.kullaniciAdi=:gelenKAd and K.parola=:parola");  //girilen kullanıcı adı ve parola ile veri tabanında kayıt olup olmadığını sorguluyoruz
                query.setParameter("gelenKAd", GirisKAdiText.getText()); //hql sorgusunda parametre olarak kullandığımız gelenKAd'a kullanıcının girdiği kullanıcı adını atıyoruz
                query.setParameter("parola", parola);   //hql sorgusunda parametre olarak kullandığımız parola'ya kullanıcının girdiği parolayı atıyoruz
                Kullanici kullaniciKontrol = (Kullanici) query.uniqueResult();   //tek bir veri alacağımız için uniqueResult() metodu ile sorguyu gerçekleştiriyoruz
                if (kullaniciKontrol == null) { //eğer veri tabanında böyle bir kayıt yoksa uyarıyoruz
                    girisBilgiL.setText("Kullanıcı adı ya da parola yanlış");
                } else {
                    LoginData.setKullaniciBilgi(new Kullanici(kullaniciKontrol.getId(), kullaniciKontrol.getAd(), kullaniciKontrol.getParola(), kullaniciKontrol.getCuzdan())); //giriş yapan kullanıcıya ait bilgileri static bir değişkende tutuyoruz
                    urunYukle.doldur(urunP, urunSepet, cuzdanBilgiL, buildSessionFactory());    //urunPanel sayfasına yönlendirileceği için bu paneli hazırlıyoruz
                    girisBilgiL.setText("");
                    panelDegistir(marketPanel);
                }
                sessionObj.getTransaction().commit();
            } catch (Exception sqlException) {
                if (null != sessionObj.getTransaction()) {
                    sessionObj.getTransaction().rollback();
                }
                sqlException.printStackTrace();
            }
        }
    }//GEN-LAST:event_girisYapButtonActionPerformed

