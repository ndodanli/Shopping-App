    private void yukleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yukleButtonActionPerformed
        //bakiye yüklemek için kullanılan buton için kullanılan ActionPerformed
        if (kartNumText.getText().isEmpty() || sKText.getText().isEmpty() || cvvText.getText().isEmpty() || bakiyeMiktarText.getText().isEmpty()) {
            bakiyeBilgiL.setText("Boş bilgi bırakmayınız");
        } else {
            Kullanici kullanici = LoginData.getKullaniciBilgi();
            float yeniBakiye = kullanici.getCuzdan() + Float.parseFloat(bakiyeMiktarText.getText());
            try {
                sessionObj = sessionFactoryObj.openSession();
                sessionObj.beginTransaction();
                String hql = "update Kullanici set kullanici_cuzdan=:yeniBakiye where id=:kullaniciId"; //kullanıcının cüzdanını güncelleyen hql sorgusu
                Query query = sessionObj.createQuery(hql);
                query.setParameter("kullaniciId", kullanici.getId());
                query.setParameter("yeniBakiye", yeniBakiye);
                int result = query.executeUpdate();
                kullanici.setCuzdan(yeniBakiye);
                LoginData.setKullaniciBilgi(kullanici);
                bakiyeCuzdanBilgiL.setText(String.valueOf(yeniBakiye));
                bakiyeBilgiL.setText("Yükleme işlemi başarılı");
                bakiyePanel.revalidate();
                bakiyePanel.repaint();
                sessionObj.getTransaction().commit();
            } catch (Exception sqlException) {
                if (null != sessionObj.getTransaction()) {
                    sessionObj.getTransaction().rollback();
                }
                sqlException.printStackTrace();
            } finally {
                if (sessionObj != null) {
                    sessionObj.close();
                }
            }
        }
    }//GEN-LAST:event_yukleButtonActionPerformed

